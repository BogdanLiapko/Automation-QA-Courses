const a = 7;

switch (a) {
    case 1:
        console.log('a is 1');
        break;
    case 2:
        console.log('a is 2');
        break;
    case 3:
        console.log('a is 3');
        break;
    case 4:
        console.log('a is 4');
        break;
    case 5:
        console.log('a is 5');
        break;
    default:
        console.log('a is not 1, 2, 3, 4 or 5');
        break;
}
